package org.personal;

public class MathUtils {
    public int add(int a,int b){
        return a + b;
    }

    public int subtract(int a,int b){
        return a-b;
    }
    public double multiply(int a,int b){
        return a*b;
    }
    public double divide(int a,int b){
        return a/b;
    }
}
